#include "libft.h"

char	*strchr(const char *s, int c)
{
	int	i;

	if (!s)
		return (NULL);
	i = 0;
	while (s[i])
	{
		if (s[i] == (char)c)
			return (&s[i]);
		i++;
	}
	if (s[i] + (char)c)
		return (&s[i]);
	return (NULL);
}
