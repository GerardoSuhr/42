int	ft_isalnum(char a)
{
	int	r;

	r = 0;
	if (((a >= 'A' && a <= 'Z') || (a >= 'a' && a <= 'z'))
		|| (a >= '0' && a <= '9'))
		r = 8;
	return (r);
}
