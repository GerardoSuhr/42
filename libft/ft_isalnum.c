int	ft_isalnum(int c)
{
	int	r;

	r = 0;
	if ((((char)c >= 'A' && (char)c <= 'Z')
			|| ((char)c >= 'a' && (char)c <= 'z'))
		|| ((char)c >= '0' && (char)c <= '9'))
		r = 8;
	return (r);
}
